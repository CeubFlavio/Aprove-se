export function translateOptionIndex(index) {

    switch (index) {
        case 0:
            translated = 'A';
            break;
        case 1:
            translated = 'B';
            break;
        case 2:
            translated = 'C';
            break;
        case 3:
            translated = 'D';
            break;
        case 4:
            translated = 'E';
            break;
    }

    return translated;
}